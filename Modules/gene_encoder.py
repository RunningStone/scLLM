class Gene2VecPositionalEmbedding(nn.Module):
    def __init__(self, gene2vec_weight:str=None, max_seq_len:int=16907):
        super().__init__()
        if gene2vec_weight is not None:
            gene2vec_weight = np.load(gene2vec_weight)
        else:
            max_seq_len = max_seq_len -1 
